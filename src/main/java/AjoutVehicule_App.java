    private void registerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_registerActionPerformed
        String marque = marqueText.getText();
        String modele = modeleText.getText();
        String anneeSortie = anneeSortieText.getText();
        String prixAchat = prixAchatText.getText();
        String kilom = kilomText.getText();
        String porte = porteText.getText();
        String puissFisc = puissFiscText.getText();

        String rqt = "BEGIN; "
                + "INSERT INTO couleur(nomCouleur) "
                + "VALUES('" + couleur_jComboBox.getSelectedItem() + "'); "
                
                + "INSERT INTO moteur(carburant, puissanceFiscale, boiteVitesse) "
                + "VALUES('" + carburant_jComboBox.getSelectedItem() + "', '" + puissFisc + "', '" + boiteVit_jComboBox.getSelectedItem() + "'); "
                
                + "INSERT INTO vehicule(marque, modele, anneeSortie, prixAchat, kilometrage, nbrePortes, idCouleur, idMoteur) "
                + "VALUES('" + marque + "', '" + modele + "' ,'" + anneeSortie + "', '" + prixAchat + "', '" + kilom + "', '" + porte + "', LAST_INSERT_ID(), LAST_INSERT_ID()); "
                
                + "COMMIT;";

        try {
            st = DBC.ObtenirConnexion().createStatement();
            st.executeUpdate(rqt);
            JOptionPane.showMessageDialog(null, "Votre véhicule a bien été ajouté !");

            marqueText.setText("");
            modeleText.setText("");
            anneeSortieText.setText("");
            prixAchatText.setText("");
            kilomText.setText("");

        } catch (Exception ex) {
            System.out.println("Error: " + ex);
        }
    }//GEN-LAST:event_registerActionPerformed

