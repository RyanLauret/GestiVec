    private void couleurComboBox() {
        String rqt = "SELECT nomCouleur FROM couleur;";

        try {
            st = DBC.ObtenirConnexion().createStatement();
            ResultSet resultat = st.executeQuery(rqt);
            while (resultat.next()) {
                String couleur = resultat.getString("nomCouleur");
                couleur_jComboBox.addItem(couleur);
            }

        } catch (Exception ex) {
            System.out.println("Error: " + ex);
        }
    }

